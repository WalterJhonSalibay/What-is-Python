# AccessingElements.py

# Define the list of fruits
fruits = ["apple", "banana", "cherry", "date"]

# Access the first element
first_fruit = fruits[0]  # First element
print("The first fruit is:", first_fruit)

# Access the second element
second_fruit = fruits[1]  # Second element
print("The second fruit is:", second_fruit)

# Access the last element
last_fruit = fruits[-1]  # Last element
print("The last fruit is:", last_fruit)